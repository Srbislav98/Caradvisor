package com.sw.projekat.controller;
import java.util.Collection;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import com.sw.projekat.dto.UserDTO;
import com.sw.projekat.dto.UserLoginDTO;
import com.sw.projekat.dto.UserTokenState;
import com.sw.projekat.exception.ResourceConflictException;
